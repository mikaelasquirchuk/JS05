// Write an if statement that writes Hello World in different languages (i.e. if the language is english, write Hello World, if the language is French write something romantic)

var language = "Gibberish";

if (language === "English") {
    console.log("Hello there");
} else if (language === "Spanish") {
    console.log("Hola chica");
} else if (language === "French") {
    console.log("Bonjour ma cherie;");
} else if (language === "Gibberish") {
    console.log("Shematzcha Blahablah")
} else {
    console.log("That's not a language");
}