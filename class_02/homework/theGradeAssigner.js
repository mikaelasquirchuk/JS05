//Write an if statement that gives users a score (A, B, C, D, or F) based on the test results


var userScore = 50;

if (userScore >= 85) {
    console.log("User gets A");
} else if (userScore >= 75) {
    console.log("User gets B");
} else if (userScore >= 65) {
    console.log("User gets C");
} else if (userScore >= 50) {
    console.log("User gets D");
} else {
    console.log("User gets F");
}